public class DoubleIntCasting { 
public static void main(String[] args) {
double doubleVar = 9.78;
int intFromDouble = (int) doubleVar; 
System.out.println("Double value: " + doubleVar); System.out.println("Double cast to int: " + intFromDouble);
int intVar = 42; 
double doubleFromInt = intVar;
System.out.println("Int value: " + intVar); 
System.out.println("Int cast to double: " + doubleFromInt); 
} 
} 
